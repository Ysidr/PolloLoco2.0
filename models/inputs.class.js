/**
 * @class Inputs
 * @description Captures the current state of player control inputs.
 */
class Inputs {
    /**
     * @type {boolean}
     * @description Indicates whether the left movement input is active.
     */
    LEFT = false;
    /**
     * @type {boolean}
     * @description Indicates whether the right movement input is active.
     */
    RIGHT = false;
    /**
     * @type {boolean}
     * @description Indicates whether the jump input is active.
     */
    JUMP = false;
    /**
     * @type {boolean}
     * @description Indicates whether the shoot input is active.
     */
    SHOOT = false;
    /**
     * @type {boolean}
     * @description Indicates whether the trade input is active.
     */
    TRADE = false;
}
